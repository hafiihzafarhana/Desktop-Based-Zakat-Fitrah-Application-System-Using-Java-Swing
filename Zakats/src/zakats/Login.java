    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        
        try {
            if(username.getText().isEmpty() || passwords.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Form Harus Terisi");
            }
            
            else{
            String userName = username.getText();
            String password = passwords.getText();
            String Sql = "SELECT * FROM admin WHERE username='"+userName+"' AND password='"+password+"'";
            Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/dbzakat", "root", "");
            St1 = Con.createStatement();
            Rs1 = St1.executeQuery(Sql);
            
                if(Rs1.next()){
                    Muzaki.IdAdminMuzaki.setText(Rs1.getString(1));
                    Mustahik.idMustahikAdmin.setText(Rs1.getString(1));
                    
                    CountProd();
                    PreparedStatement Pstt = Con.prepareStatement("insert into logging values (?,?,?,?,?)");
                    DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss");
                    LocalDateTime now = LocalDateTime.now();
                    Pstt.setInt(1, PrNum);
                    Pstt.setInt(2, Rs1.getInt(1));
                    Pstt.setString(3, Rs1.getString(3));
                    Pstt.setString(4, Rs1.getString(2));
                    Pstt.setString(5, dtf.format(now));
                    int row = Pstt.executeUpdate();
                    
                    new Index().setVisible(true);
                    this.dispose();
                }
                else{
                    JOptionPane.showMessageDialog(this, "Username Atau Password Salah");
                }
            }
          
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

